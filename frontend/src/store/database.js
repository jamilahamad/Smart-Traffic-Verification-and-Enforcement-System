// ---------- GENERATE UNIQUE IDS ----------
let idCounter = 1000;
export function generateId(prefix = 'STR') {
    idCounter++;
    return `${prefix}-${Date.now().toString(36).toUpperCase()}-${idCounter}`;
}
export function generateCaseId() {
    const yr = new Date().getFullYear();
    const num = Math.floor(Math.random() * 900000) + 100000;
    return `EC-${yr}-${num}`;
}
